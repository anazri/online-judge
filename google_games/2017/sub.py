def solve(grid, x, y, moves):
    if x < 0 or x >= len(grid) or y < 0 or y >= len(grid[x]) or grid[x][y] == 1:
        return False
    for move in moves:
        if x < 0 or x >= len(grid) or y < 0 or y >= len(grid[x]) or grid[x][y] == 1:
            return False

        x += dx[move]
        y += dy[move]

    if x < 0 or x >= len(grid) or y < 0 or y >= len(grid[x]) or grid[x][y] == 1:
        return False

    print(x, y)
    return True


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);

    int n, m;
    cin >> n >> m;
    set<string> seen;
    string s;
    for (int d = 1; d <= m; ++d) {
        cin >> s;
        seen.insert(s);
        if (seen.size() == n) {
            printf("%d\n", d);
            return 0;
        }
    }

    printf("paradox avoided\n");
    return 0;
}
